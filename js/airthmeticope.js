let x = 5;
let y = 3;

// addition
console.log('x + y = ', x + y); 

// subtraction
console.log('x - y = ', x - y); 

// multiplication
console.log('x * y = ', x * y);  

// division
console.log('x / y = ', x / y); 

// remainder
console.log('x % y = ', x % y);   

// increment
console.log('++x = ', ++x);
console.log('x++ = ', x++); 
console.log('x = ', x);     

// decrement
console.log('--x = ', --x); 
console.log('x-- = ', x--); 
console.log('x = ', x);     

//exponentiation
console.log('x ** y =', x ** y);