const a = 5, b = 2, c = 'hello';
//equal to operator
console.log(a == 5);     
console.log(b == '2');   
console.log(c == 'Hello'); 


// not equal operator
console.log(a != 2);
console.log(b != 'Hello'); 

// strict equal operator
console.log(a === 2); 
console.log(a === '2'); 

console.log(a > 2);

console.log(a >= 3);

// less than or equal operator
console.log(a <= 3);
console.log(a <= 2);